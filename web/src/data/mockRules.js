// Compute coverage stats per tactic
export function computeCoverageStats(coverage) {
  const stats = {}
  for (const [tactic, techniques] of Object.entries(coverage)) {
    const total = techniques.length
    const full = techniques.filter((t) => t.coverage === 'full').length
    const partial = techniques.filter((t) => t.coverage === 'partial').length
    const none = techniques.filter((t) => t.coverage === 'none').length
    const pct = total > 0 ? Math.round(((full + partial * 0.5) / total) * 100) : 0
    stats[tactic] = { total, full, partial, none, pct }
  }
  return stats
}

// Generate ATT&CK Navigator JSON export
export function generateNavigatorJSON(coverage) {
  const techniques = []
  for (const [tactic, techs] of Object.entries(coverage)) {
    for (const tech of techs) {
      const score = tech.coverage === 'full' ? 3 : tech.coverage === 'partial' ? 2 : 0
      const color = tech.coverage === 'full' ? '#22c55e' : tech.coverage === 'partial' ? '#f59e0b' : ''
      techniques.push({
        techniqueID: tech.id,
        tactic: tactic.toLowerCase().replace(/ /g, '-'),
        score,
        color,
        comment: tech.coverage === 'none' ? 'No coverage' : `Coverage: ${tech.coverage}`,
        enabled: true,
      })
    }
  }

  return {
    name: 'SentinelSIEM Coverage',
    versions: { attack: '14', navigator: '4.9.1', layer: '4.5' },
    domain: 'enterprise-attack',
    description: 'ATT&CK coverage layer exported from SentinelSIEM',
    techniques,
    gradient: {
      colors: ['#ef4444', '#f59e0b', '#22c55e'],
      minValue: 0,
      maxValue: 3,
    },
    legendItems: [
      { label: 'No Coverage', color: '#ef4444' },
      { label: 'Partial', color: '#f59e0b' },
      { label: 'Full', color: '#22c55e' },
    ],
  }
}
